package Operator;

public class IncreamentDecreament {
    public static void main(String[] args) {

        int a=4;
        int b=7;

        System.out.println(a);
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);
        System.out.println(b);
        b--;
        System.out.println(b);
        --b;
        System.out.println(b);



    }


}
